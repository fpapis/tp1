package fr.ul.miage.papis.florent;

public class CsvDate {
	
	private int numero;
	private int mois;
	private int annee;
	private int heure;
	private int minute;
	
	public CsvDate(int numero, int mois, int annee, int heure, int minute) {
		this.numero = numero;
		this.mois = mois;
		this.annee = annee;
		this.heure = heure;
		this.minute = minute;
	}

}
